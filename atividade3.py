# Declaração das variáveis a partir de inputs do usuário
num1 = int(input("Digite o primeiro número:"))
num2 = int(input("Digite o segundo número:"))

# Realização das quatro operações básicas com os valores recebidos
soma = num1 + num2
sub = num1 - num2
mult = num1 * num2
div = num1 / num2

# Impressão dos resultados concatenados a um texto
print("====================")
print(f"Soma: {soma}")
print(f"Subtração: {sub}")
print(f"Multiplicação: {mult}")
print(f"Divisão: {div}")
print("====================")