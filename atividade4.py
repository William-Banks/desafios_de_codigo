# Declarações de variáveis a partir do input do usuário
nome = input("Digite o seu nome: ")
idade = int(input("Digite a sua idade: "))

# Impressão do nome e idade concatenados
print("====================")
print(f"O seu nome é: {nome}")
print(f"A sua idade é: {idade} anos")
print("====================")

# Declaração das variáveis de número a partir dos valores digitados pelo usuário
num1 = int(input("Digite o primeiro número para ser somado: "))
num2 = int(input("Digite o segundo número para ser somado: "))

# Declaração da variável de soma
soma = num1 + num2

# Impressão da soma dos valores recebidos
print("====================")
print(f"A soma dos números é: {soma}")
print("====================")
